const Groq = require('groq-sdk');
const mongoose = require('mongoose');
const Playlist = require('../models/Playlist');
const mockDb = require('../services/mockDb');
const youtubeService = require('../services/youtubeService');

// Helper: get a configured Groq client
function getGroqClient() {
    const apiKey = process.env.GROQ_API_KEY;
    if (!apiKey) {
        throw new Error('GROQ_API_KEY is not configured in the server environment.');
    }
    return new Groq({ apiKey });
}

// Helper: parse JSON safely from model output (strip markdown fences if present)
function parseJsonResponse(text) {
    let cleaned = (text || '').trim();
    if (cleaned.startsWith('```')) {
        cleaned = cleaned.replace(/^```(json)?/, '').replace(/```$/, '').trim();
    }
    const parsed = JSON.parse(cleaned);
    if (!Array.isArray(parsed)) throw new Error('Response is not an array');
    return parsed;
}

const generatePlaylist = async (req, res) => {
    try {
        const { prompt, count = 10, isPublic = false, title } = req.body;
        const userId = req.user._id;

        if (!prompt) {
            return res.status(400).json({ error: 'Prompt is required' });
        }

        const groq = getGroqClient();

        // 1. Call Groq API (llama-3.3-70b-versatile — free tier)
        const completion = await groq.chat.completions.create({
            model: 'llama-3.3-70b-versatile',
            messages: [
                {
                    role: 'system',
                    content: `You are an expert music curator. Generate exactly ${count} songs that perfectly match the user's prompt.
You must return ONLY a valid JSON array of objects, each with exactly two string properties: "title" and "artist".
Do not wrap the JSON in markdown code blocks. Output raw JSON only.`
                },
                {
                    role: 'user',
                    content: `Generate ${count} songs for this vibe/mood: "${prompt}"`
                }
            ],
            temperature: 0.8,
            max_tokens: 2048,
            response_format: { type: 'json_object' }
        });

        const rawText = completion.choices[0]?.message?.content || '';

        let aiSongs = [];
        try {
            // Groq json_object mode wraps in an object — handle both array and wrapped
            const parsed = JSON.parse(rawText);
            if (Array.isArray(parsed)) {
                aiSongs = parsed;
            } else {
                // Find the first array value in the object
                const firstArray = Object.values(parsed).find(v => Array.isArray(v));
                if (firstArray) aiSongs = firstArray;
                else throw new Error('No array found in response');
            }
        } catch (e) {
            console.error('Failed to parse Groq response:', rawText);
            return res.status(500).json({ error: 'Failed to parse AI response' });
        }

        // 2. Map AI songs to YouTube results
        const playlistSongs = [];
        for (const aiSong of aiSongs) {
            try {
                const query = `${aiSong.title} ${aiSong.artist} official audio`;
                const searchResults = await youtubeService.searchMusic(query);
                if (searchResults && searchResults.length > 0) {
                    playlistSongs.push(searchResults[0]);
                }
            } catch (err) {
                console.warn(`Failed to fetch YouTube details for ${aiSong.title} by ${aiSong.artist}`);
            }
        }

        if (playlistSongs.length === 0) {
            console.warn('No songs resolved, falling back to trending music...');
            try {
                const trending = await youtubeService.getTrendingMusic();
                playlistSongs.push(...trending.slice(0, count));
            } catch (err) {
                return res.status(500).json({ error: 'Failed to find any playable songs for this prompt' });
            }
        }

        // 3. Create Playlist in MongoDB
        const playlistName = title || `AI Playlist: ${prompt.slice(0, 30)}${prompt.length > 30 ? '...' : ''}`;
        const description = `Generated by Melodia AI. Prompt: "${prompt}"`;
        const coverImage = playlistSongs[0]?.thumbnail || '';

        let savedPlaylist;
        if (mongoose.connection.readyState === 1) {
            const newPlaylist = new Playlist({
                owner: userId,
                title: playlistName,
                description,
                isPublic: isPublic,
                songs: playlistSongs,
                coverImage,
                playlistType: 'ai',
                isAIGenerated: true
            });
            savedPlaylist = await newPlaylist.save();
        } else {
            savedPlaylist = mockDb.createPlaylist(userId, {
                title: playlistName,
                description,
                isPublic,
                songs: playlistSongs,
                coverImage,
                playlistType: 'ai',
                isAIGenerated: true
            });
        }

        res.status(201).json(savedPlaylist);
    } catch (error) {
        console.error('Error generating smart playlist:', error);
        res.status(500).json({ error: error.message || 'Internal server error while generating playlist' });
    }
};

const getAutoplayRecommendations = async (req, res) => {
    try {
        const { title, artist } = req.body;
        if (!title) {
            return res.status(400).json({ error: 'Title is required for autoplay context' });
        }

        const groq = getGroqClient();

        const completion = await groq.chat.completions.create({
            model: 'llama-3.3-70b-versatile',
            messages: [
                {
                    role: 'system',
                    content: `You are a music recommendation engine.
Return ONLY a valid JSON array of exactly 5 songs similar to the requested track.
Each object must have exactly two string properties: "title" and "artist".
Do not wrap the JSON in markdown code blocks. Output raw JSON array only.`
                },
                {
                    role: 'user',
                    content: `Recommend 5 songs similar to "${title}" by "${artist || 'Unknown Artist'}".`
                }
            ],
            temperature: 0.8,
            max_tokens: 512
        });

        const rawText = completion.choices[0]?.message?.content || '';

        let aiSongs = [];
        try {
            aiSongs = parseJsonResponse(rawText);
        } catch (e) {
            console.error('Failed to parse Groq autoplay response:', rawText);
            return res.status(500).json({ error: 'Failed to parse recommendations' });
        }

        const resolvedSongs = [];
        for (const aiSong of aiSongs) {
            try {
                const query = `${aiSong.title} ${aiSong.artist} official audio`;
                const searchResults = await youtubeService.searchMusic(query);
                if (searchResults && searchResults.length > 0) {
                    resolvedSongs.push({
                        id: searchResults[0].id,
                        videoId: searchResults[0].videoId,
                        title: searchResults[0].title,
                        artist: searchResults[0].artist,
                        thumbnail: searchResults[0].thumbnail,
                        duration: searchResults[0].duration
                    });
                }
            } catch (err) {
                console.warn(`Autoplay YouTube resolution failed for ${aiSong.title}`);
            }
        }

        res.status(200).json(resolvedSongs);
    } catch (error) {
        console.error('Error generating autoplay recommendations:', error);
        res.status(500).json({ error: 'Failed to generate recommendations' });
    }
};

