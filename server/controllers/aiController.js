const generateDiscoverWeekly = async (req, res) => {
    try {
        const userId = req.user._id;
        
        let likedSongs = [];
        let playlists = [];
        
        if (mongoose.connection.readyState === 1) {
            const User = require('../models/User');
            const userObj = await User.findById(userId);
            likedSongs = userObj?.likedSongs || [];
            playlists = await Playlist.find({ owner: userId });
        } else {
            const userObj = mockDb.getUser(req.user.firebaseUid || req.user._id);
            likedSongs = userObj?.likedSongs || [];
            playlists = mockDb.getPlaylists(userId);
        }

        const likedSongList = likedSongs.map(s => `"${s.title}" by ${s.artist}`).join(', ');
        
        const genres = [];
        const tags = [];
        const artistCounts = {};
        
        likedSongs.forEach(s => {
            if (s.artist) {
                artistCounts[s.artist] = (artistCounts[s.artist] || 0) + 1;
            }
        });

        playlists.forEach(p => {
            if (p.genre) genres.push(p.genre);
            if (p.tags) tags.push(...p.tags);
            p.songs?.forEach(s => {
                const artist = s.artist || s.channelTitle;
                if (artist) {
                    artistCounts[artist] = (artistCounts[artist] || 0) + 1;
                }
            });
        });

        const favoriteArtists = Object.entries(artistCounts)
            .sort((a, b) => b[1] - a[1])
            .slice(0, 5)
            .map(entry => entry[0])
            .join(', ');

        const favoriteGenres = [...new Set(genres)].slice(0, 3).join(', ');
        const favoriteTags = [...new Set(tags)].slice(0, 5).join(', ');

        let promptText = '';
        const hasHistory = likedSongs.length > 0 || playlists.length > 0;
        
        if (hasHistory) {
            promptText = `Based on this user's music profile:
- Liked Songs: ${likedSongList.slice(0, 1000)}
- Favorite Artists: ${favoriteArtists}
- Favorite Genres: ${favoriteGenres}
- Mood Tags: ${favoriteTags}

Recommend exactly 20 songs they are most likely to enjoy. The songs should be fresh, diverse, and align with their taste.
Do not recommend songs they already like. Return ONLY a valid JSON array of objects, each with exactly two string properties: "title" and "artist".
Do not wrap the JSON in markdown code blocks. Output raw JSON only.`;
        } else {
            promptText = `Recommend exactly 20 popular and trending hit songs across diverse genres.
Return ONLY a valid JSON array of objects, each with exactly two string properties: "title" and "artist".
Do not wrap the JSON in markdown code blocks. Output raw JSON only.`;
        }

        let aiSongs = [];
        let geminiError = false;
        try {
            const { GoogleGenAI } = require('@google/genai');
            const ai = new GoogleGenAI({ apiKey: process.env.GEMINI_API_KEY });
            
            const response = await ai.models.generateContent({
                model: 'gemini-2.5-flash',
                contents: promptText,
                config: {
                    responseMimeType: 'application/json',
                    responseSchema: {
                        type: 'ARRAY',
                        items: {
                            type: 'OBJECT',
                            properties: {
                                title: { type: 'STRING' },
                                artist: { type: 'STRING' }
                            },
                            required: ['title', 'artist']
                        }
                    }
                }
            });
            const text = response.text || '';
            aiSongs = JSON.parse(text);
        } catch (err) {
            console.error('Gemini Discover Weekly recommendation failed, using fallback:', err);
            geminiError = true;
        }

        let playlistSongs = [];
        if (!geminiError && aiSongs.length > 0) {
            for (const aiSong of aiSongs) {
                try {
                    const query = `${aiSong.title} ${aiSong.artist} official audio`;
                    const searchResults = await youtubeService.searchMusic(query);
                    if (searchResults && searchResults.length > 0) {
                        playlistSongs.push(searchResults[0]);
                    }
                } catch (err) {
                    console.warn(`YouTube search failed for Discover Weekly track: ${aiSong.title}`);
                }
            }
        }

        if (playlistSongs.length < 5) {
            console.warn('Resolving with trending music fallback...');
            try {
                const trending = await youtubeService.getTrendingMusic();
                playlistSongs = trending.slice(0, 20);
            } catch (err) {
                console.warn('YouTube trending failed, using cached trending fallback...');
                playlistSongs = CACHED_TRENDING_SONGS;
            }
        }

        if (playlistSongs.length > 20) {
            playlistSongs = playlistSongs.slice(0, 20);
        }

        const title = 'Discover Weekly';
        const description = 'Your weekly mixtape of fresh music, customized to your taste. Generated by Melodia AI.';
        const coverImage = playlistSongs[0]?.thumbnail || 'https://images.unsplash.com/photo-1614613535308-eb5fbd3d2c17?auto=format&fit=crop&q=80&w=500';

        let discoverPlaylist;
        if (mongoose.connection.readyState === 1) {
            discoverPlaylist = await Playlist.findOne({ owner: userId, title: 'Discover Weekly' });
            if (discoverPlaylist) {
                discoverPlaylist.songs = playlistSongs;
                discoverPlaylist.coverImage = coverImage;
                discoverPlaylist.description = description;
                discoverPlaylist.playlistType = 'ai';
                discoverPlaylist.isAIGenerated = true;
                await discoverPlaylist.save();
            } else {
                discoverPlaylist = new Playlist({
                    owner: userId,
                    title,
                    description,
                    songs: playlistSongs,
                    coverImage,
                    privacy: 'Private',
                    playlistType: 'ai',
                    isAIGenerated: true
                });
                await discoverPlaylist.save();
            }
        } else {
            const allMockPlaylists = mockDb.getPlaylists(userId);
            discoverPlaylist = allMockPlaylists.find(p => p.title === 'Discover Weekly');
            if (discoverPlaylist) {
                discoverPlaylist.songs = playlistSongs;
                discoverPlaylist.coverImage = coverImage;
                discoverPlaylist.description = description;
                discoverPlaylist.playlistType = 'ai';
                discoverPlaylist.isAIGenerated = true;
                await discoverPlaylist.save();
            } else {
                discoverPlaylist = mockDb.createPlaylist(userId, {
                    title,
                    description,
                    songs: playlistSongs,
                    coverImage,
                    privacy: 'Private',
                    playlistType: 'ai',
                    isAIGenerated: true
                });
            }
        }

        res.status(200).json({ success: true, playlist: discoverPlaylist });
    } catch (error) {
        console.error('Error generating discover weekly playlist:', error);
        res.status(500).json({ error: error.message || 'Failed to generate Discover Weekly' });
    }
};

